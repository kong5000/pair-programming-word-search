const chai = require('chai');
const assert = chai.assert;

const wordSearch = require('../wordsearch.js')

describe("#wordSearch()", function() {
  it("should return false if the word is not present", function() {
    const result = wordSearch([
      ['A', 'W', 'C', 'F', 'Q', 'U', 'A', 'L'],
      ['S', 'E', 'I', 'N', 'F', 'E', 'L', 'D'],
      ['Y', 'F', 'C', 'F', 'Q', 'U', 'A', 'L'],
      ['H', 'M', 'J', 'T', 'E', 'V', 'R', 'G'],
      ['W', 'H', 'C', 'S', 'Y', 'E', 'R', 'L'],
      ['B', 'F', 'R', 'E', 'N', 'E', 'Y', 'B'],
      ['U', 'B', 'T', 'W', 'A', 'P', 'A', 'I'],
      ['O', 'D', 'C', 'A', 'K', 'U', 'A', 'S'],
      ['E', 'Z', 'K', 'F', 'Q', 'U', 'A', 'L'],
    ], 'FRANK')

    assert.isFalse(result);
  });

  it("should return true if the word is present", function() {
    const result = wordSearch([
      ['A', 'W', 'C', 'F', 'Q', 'U', 'A', 'L'],
      ['S', 'E', 'I', 'N', 'F', 'E', 'L', 'D'],
      ['Y', 'F', 'C', 'F', 'Q', 'U', 'A', 'L'],
      ['H', 'M', 'J', 'T', 'E', 'V', 'R', 'G'],
      ['W', 'H', 'C', 'S', 'Y', 'E', 'R', 'L'],
      ['B', 'F', 'R', 'E', 'N', 'E', 'Y', 'B'],
      ['U', 'B', 'T', 'W', 'A', 'P', 'A', 'I'],
      ['O', 'D', 'C', 'A', 'K', 'U', 'A', 'S'],
      ['E', 'Z', 'K', 'F', 'Q', 'U', 'A', 'L'],
    ], 'SEINFELD')

    assert.isTrue(result);
  });

  it("should return true if the word is present vertically", function() {
    const result = wordSearch([
      ['C', 'W', 'C', 'F', 'Q', 'U', 'A', 'L'],
      ['O', 'E', 'I', 'N', 'F', 'E', 'L', 'D'],
      ['D', 'F', 'C', 'F', 'Q', 'U', 'A', 'L'],
      ['E', 'M', 'J', 'T', 'E', 'V', 'R', 'G'],
      ['W', 'H', 'C', 'S', 'Y', 'E', 'R', 'L'],
      ['B', 'F', 'R', 'E', 'N', 'E', 'Y', 'B'],
      ['U', 'B', 'T', 'W', 'A', 'P', 'A', 'I'],
      ['O', 'D', 'C', 'A', 'K', 'U', 'A', 'S'],
      ['E', 'Z', 'K', 'F', 'Q', 'U', 'A', 'L'],
    ], 'CODE')

    assert.isTrue(result);
  });

  it("should return true if the word is backwards horizontally", function() {
    const result = wordSearch([
      ['E', 'D', 'O', 'C', 'Q', 'U', 'A', 'L'],
      ['H', 'E', 'I', 'N', 'F', 'E', 'L', 'D'],
      ['E', 'F', 'C', 'F', 'Q', 'U', 'A', 'L'],
      ['E', 'M', 'J', 'T', 'E', 'V', 'R', 'G'],
      ['W', 'H', 'C', 'S', 'Y', 'E', 'R', 'L'],
      ['B', 'F', 'R', 'E', 'N', 'E', 'Y', 'B'],
      ['U', 'B', 'T', 'W', 'A', 'P', 'A', 'I'],
      ['O', 'D', 'C', 'A', 'K', 'U', 'A', 'S'],
      ['E', 'Z', 'K', 'F', 'Q', 'U', 'A', 'L'],
    ], 'CODE')

    assert.isTrue(result);
  });
  it("should return true if the word is diagonal", function() {
    const result = wordSearch([
      ['B','B','C'],
      ['D','A','F'],
      ['G','H','T']
    ], 'BAT');

    assert.isFalse(result);
  });
  it("should return true if the word is diagonal and backwards", function() {
    const result = wordSearch([
      ['T','B','C'],
      ['D','A','F'],
      ['G','H','C']
    ], 'CAT');

    assert.isFalse(result);
  });

  it("should return false if array is empty", function() {
    const result = wordSearch([], 'CODE');
    assert.isFalse(result);
  });
  it("should return false if array is null", function() {
    const result = wordSearch(null, 'CODE');
    assert.isFalse(result);
  });

});
